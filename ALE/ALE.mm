<node COLOR="#00b439" CREATED="1392563592644" ID="ID_1045220349" MODIFIED="1392566104845" TEXT="ACT/ACT-R">
<richcontent TYPE="NOTE"><html>
  <head>
    
  </head>
  <body>
    <p>
      <u><b>ACT/ACT-R: </b></u>&#160;&quot;A theory of learning and problem solving&quot;
    </p>
    <ul>
      <li>
        Allows creation of program-like models
      </li>
      <li>
        Is a&#160;&#160;cognitive architecture
      </li>
      <li>
        Is a framework
      </li>
    </ul>
    <p>
      
    </p>
    <p>
      Keypoint 1: Cognitive skills are made up of small units of &quot;goal-related&quot; knowledge. These units are represented by production rules.
    </p>
    <p>
      Theory says: we acquire cognitive skills by formulating production rules.
    </p>
    <p>
      
    </p>
    <p>
      Keypoint 2: Theory hinges on two main types of knowledge. Declarative and Procedural.
    </p>
    <p>
      &quot;Declarative knowledge by itself is inert and often quite useless&quot;
    </p>
    <p>
      
    </p>
    <p>
      Keypoint 3: How do we get to the difficult procedural knowledge?
    </p>
    <p>
      ACT says: by using declarative knowledge and relating it to tasks and/or goals. Likely facilitated by overt instruction, analogy to other known contexts.
    </p>
    <p>
      This declarative knowledge plus interpretation generates problem-solving behaviour. Knowledge complilation converts this interpretation/problem solving into production rules.
    </p>
    <p>
      
    </p>
    <p>
      <b>From ACT to Tutoring </b>
    </p>
    <ul>
      <li>
        About doing, not memorisation.
      </li>
      <li>
        Focus on procedural knowledge, turining inert declarative knowledge to appropriate action.
      </li>
    </ul>
  </body>
</html></richcontent>

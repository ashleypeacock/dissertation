<node COLOR="#990000" CREATED="1392311292663" ID="ID_1925736090" MODIFIED="1392312685724" TEXT="Interpretive Model">
<richcontent TYPE="NOTE"><html>
  <head>
    
  </head>
  <body>
    <p>
      <b>Question</b>: Why are receptive fields in V1 shaped in this way? This is a question solved by looking at the interpretive model.
    </p>
    <p>
      Question: What are the computational advantages of such receptive fields?
    </p>
    <p>
      
    </p>
    <p>
      Interpretive model is based on the <i>Efficient Coding Hypothesis: Goal of the brain through evolution is to represent images as fatefully and efficiently as possible using the neurons that it has. </i>
    </p>
    <p>
      
    </p>
    <p>
      You can add RF's and you obtain a linear combination.
    </p>
    <p>
      
    </p>
    <p>
      Could start out with a set of random receptive fields and run an efficient coding algorithm which tires to minimise the reconstruction error(the error between reconstructed &amp; actual image).
    </p>
    <p>
      
    </p>
    <p>
      With the interpretive model of V1 fields, our purpose is to:
    </p>
    <ul>
      <li>
        Give explanation why V1 receptive fields are formed the way we observe them to be
      </li>
      <li>
        Demonstrate that the perceptual processes in the brain may be formed in a way that allos for a faithful and efficient encoding of the natural environment
      </li>
      <li>
        Provide a computational model of one aspect of the brain's organization, which gives us insights that may be difficult to derive from strict empirical measurements alone
      </li>
    </ul>
  </body>
</html></richcontent>

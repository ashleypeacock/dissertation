<node COLOR="#111111" CREATED="1393166353027" ID="ID_681778078" MODIFIED="1393166523828" TEXT="What&apos;s in a neuron?">
<richcontent TYPE="NOTE"><html>
  <head>
    
  </head>
  <body>
    <p>
      Leaky bag of charged liquid.
    </p>
    <p>
      Contents of the neuron are enclosed within a cell membrane which is a lipid(fat) bilayer.
    </p>
    <p>
      The bilayer is impermeable to charged ion species such as Na+, CL- and K+.
    </p>
    <p>
      Each neiron maintains a potential difference across its membrane. Inside is about -70mv relative to outside.
    </p>
    <p>
      There are more Na+ and CL- outside, and more oganic ions higher inside (K+ and A-)
    </p>
  </body>
</html>

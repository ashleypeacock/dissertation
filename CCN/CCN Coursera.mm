      How are these oritented receptive fields obtained from center-surround receptive fields?
    </p>
    <p>
      For this we can look at the mechanistic model.
    </p>
    <p>
      
    </p>
    <p>
      Inputs from the LGN cells go into the V1(Primary visual cortex). The LGN cells are arranged to make an elongated(orientated) V1 cell. Thus, V1 RFs are created from converging LGN inputs.
    </p>
    <p>
      
    </p>
    <p>
      The V1 cell recieves input from other LGN cells as well as other V1 cells.
    </p>
